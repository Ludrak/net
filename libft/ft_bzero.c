#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tmp;

	tmp = (unsigned char *)s + n;
	while (n > 0)
	{
		n--;
		tmp--;
		*tmp = 0;
	}
}
